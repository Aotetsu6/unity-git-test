//＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝


public class CameraMove : MonoBehaviour {
    //＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝
    //自機を画面の中央にとらえ続けるようカメラに追従させる
    //＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝＝

    GameObject Player;
    GameObject mainCamera;

    //初期化
    void Start () {
        Player = GameObject.Find("Player");          //自機のオブジェクトを指定          
        mainCamera = GameObject.Find("Main Camera");//カメラオブジェクトを指定

    }

    //更新処理
    void Update () {

        //カメラ座標を自機座標のz軸-10の位置にする。
        mainCamera.transform.position = new Vector3(Player.transform.position.x, Player.transform.position.y, Player.transform.position.z - 10);

    }
}
